import { Entity, Column, PrimaryGeneratedColumn } from 'typeorm';

@Entity()
export class Blockchain {
  @PrimaryGeneratedColumn()
  id: number;

  @Column()
  blockchainId: string;

  @Column()
  currentBlock: string;

  @Column()
  blockchainStatus: string;

  @Column()
  blockchainVersion: string;
}
